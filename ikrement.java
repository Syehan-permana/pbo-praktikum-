
package Syehan_praktikum;


public class ikrement {
    public static void main(String[] args) {
        
        int a = 5;
        int b = 5;

        // Post-inkremen
        System.out.println("Post-inkremen:");
        System.out.println("Nilai awal a: " + a);
        int x = a++;  // x diberi nilai a, lalu a dinaikkan
        System.out.println("Nilai x (a++): " + x);
        System.out.println("Nilai a setelah a++: " + a);

        System.out.println();

        // Pre-inkremen
        System.out.println("Pre-inkremen:");
        System.out.println("Nilai awal b: " + b);
        int y = ++b;  // b dinaikkan dulu, lalu y diberi nilai b
        System.out.println("Nilai y (++b): " + y);
        System.out.println("Nilai b setelah ++b: " + b);
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
    }
   
}
